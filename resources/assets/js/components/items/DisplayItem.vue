<template>
    <div>
        <h1>Items</h1>

        <div class="row">
            <div class="col-md-offset-10 col-md-2">
                <router-link :to="{name: 'CreateItem'}" class="btn btn-primary">
                    Create Item
                </router-link>
            </div>
        </div>

        <br>

        <div class="table table-hover">
            <thead>
            <tr>
                <td>ID</td>
                <td>Item Name</td>
                <td>Item Price</td>
                <td>Actions</td>
            </tr>
            </thead>

            <tbody>
            <tr v-for="(item, key) in items">
                <td>{{ item.id }}</td>
                <td>{{ item.name }}</td>
                <td>{{ item.price }}</td>
                <td>
                    <router-link :to="{name: 'EditItem', params: {id: item.id}}" class="btn btn-primary">
                        Edit
                    </router-link>
                </td>
                <td>
                    <button class="btn btn-danger" v-on:click="deleteItem(item.id, key)">Delete</button>
                </td>
            </tr>
            </tbody>
        </div>
    </div>
</template>

<script>
    export default {
        data() {
            return {
                items: []
            }
        },
        created: function () {
            this.fetchItems()
        },
        methods: {
            fetchItems() {
                let url = baseURL + '/items';

                this.axios.get(url)
                    .then((response) => {
                        this.items = response.data;
                    });
            },

            deleteItem(id, key) {
                let url = baseURL + '/items/' + id;

                this.items.splice(key, 1);
                this.axios.delete(url);
            }
        }
    }
</script>
